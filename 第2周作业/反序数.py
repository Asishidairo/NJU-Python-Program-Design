"""
题目描述
编写程序，从键盘输入一个4位正整数，输出该数的反序数。反序数即原数各位上的数字颠倒次序形成的另一个整数，如1234的反序数是4321，2468的反序数是8642。需整型输出。

输入
6789

输出
9876

样例输入
1234

样例输出
4321
"""

num = input()
num = num[::-1]
num = int(num)
print(num)